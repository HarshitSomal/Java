
public class Facebook {

	//constructor overloading//
	Facebook(){
		System.out.println("Facebook0");
	}

	Facebook(int i){
		System.out.println("Facebook1");
	}
	
	
	Facebook(int i,int j){
		System.out.println("Facebook2");
	}
	
	Facebook(int i,int j,int k){
		System.out.println("Facebook3");
	}
}
